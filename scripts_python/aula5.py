"""
Tipos de dados: Boolean ou Boleanos
    Ao questionar algo em um programa de computador,
    só existem duas respostas possiveis:
    Sim (True) ou Não (False). 
    E existem varios operadores para "questionar". 
    Dentre eles o (==) que é um operador logico de igualdade que questiona se um valor é igual ao outro.
"""

print(10 == 10) # sim => True (Verdadeiro)
print(10 == 11) # nao => False (Falso)
print(type(10 == 11))  # retorna o tipo como bool de boolean ou boleano

