'''
Operador lógico NOT
usado para inverter expressões
not True = False
not False = True
'''
print (True) # retorna True
print (False) # retorna false
# invertendo com o operador not
print (not True) #zero seria false, mas invertendo com o not se torna true
print (not False) # 1 seria tru, mas invertendo com o not se torna false.