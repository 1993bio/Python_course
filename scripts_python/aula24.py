# em python podemos chegar um valor em uma string da seguinte maneira. 
print ('é' in nome) # se encontrar retorna True, se retornar false é porque não foi possivel encontrar o valor correspondete
print ('z' in nome) # se encontrar retorna True, se retornar false é porque não foi possivel encontrar o valor correspondete

# invertendo o valor da checagem com (not in)
print ('z'  not in nome) # sabemos que z não tem na string "André", sendo uma condição, false, mas o not in faz a expressã ode verificação ser verdadeira.








