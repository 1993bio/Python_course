'''
Operações aritmétimas em python
'''

adicao = 10 + 10
print('adicao:', adicao)

subtracao = 10 - 5
print('subtracao:', subtracao)

multiplicacao = 10 * 10
print('multiplicacao:', multiplicacao)

divisao = 10 / 2.4 # sempre retorna um numero float 
print('divisao:', divisao)

divisao_inteira = 10 // 2.4
print('divisao inteira:', divisao_inteira)

exponenciacao = 2**10
print('Exponenciação:', exponenciacao)

modulo = 55 % 2
print('Módulo:', modulo)

# exemplos de uso do módulo
print(16 % 8 == 0) # par se o resto da divisã ode um nuemro for 0 o contrario é impar
print(16 % 2 == 0) # par
print(16 % 3 == 0) # impar, porque o resto da divisão foi 1


